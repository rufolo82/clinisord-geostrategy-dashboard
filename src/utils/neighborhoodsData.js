// Obtener información de barrio por código postal
export const getNeighborhoodByPostalCode = (postalCode, city) => {
  const code = postalCode.toString().padStart(5, '0');
  
  // Buscar en cada ciudad
  if (city === 'Madrid' || city === 'madrid') {
    return madridNeighborhoods[code] || null;
  } else if (city === 'Barcelona' || city === 'barcelona') {
    return barcelonaNeighborhoods[code] || null;
  } else if (city === 'Valencia' || city === 'valencia') {
    return valenciaNeighborhoods[code] || null;
  } else if (city === 'Sevilla' || city === 'sevilla') {
    return sevillaNeighborhoods[code] || null;
  }
  
  return null;
};

// Obtener todos los barrios de una ciudad
export const getAllNeighborhoodsByCity = (city) => {
  if (city === 'Madrid' || city === 'madrid') {
    return madridNeighborhoods;
  } else if (city === 'Barcelona' || city === 'barcelona') {
    return barcelonaNeighborhoods;
  } else if (city === 'Valencia' || city === 'valencia') {
    return valenciaNeighborhoods;
  } else if (city === 'Sevilla' || city === 'sevilla') {
    return sevillaNeighborhoods;
  }
  
  return {};
};

// Obtener información de barrio con nombre de ciudad
export const getNeighborhoodInfo = (postalCode) => {
  const neighborhoods = { ...madridNeighborhoods, ...barcelonaNeighborhoods, ...valenciaNeighborhoods, ...sevillaNeighborhoods };
  const code = postalCode.toString().padStart(5, '0');
  return neighborhoods[code] || null;
};
